	CLO
	JMP START
	DB 0		;VARIABLE A = 0
	DB 0		;VARIABLE B = 0
	
START:
	MOV AL,0	;A=0
	MOV [03],AL
	MOV BL,0	;B=0
	MOV [04],BL

PRELOOP:
	CMP AL,3	;A==3?
	JZ ADD		;IF A IS 3 GO TO ADD
	INC AL		;IF NO -> A++
	JMP PRELOOP	;REPEAT PRELOOP


LOOP:			;FOR LOOP
	CMP AL, 5	;A<=5?
	JZ OUTPUT	;IF A>=6 GO TO OUTPUT
	INC AL		;A++
	JMP LOOP	;REPEAT LOOP

ADD:	
	INC AL		;A++
	ADD BL,3	;B+=3
	JMP LOOP	;CONTINUE TO LOOP

OUTPUT:	
	ADD AL,30	;CONVERT A AND B VALUES INTO HEXA-CODE
	MOV [C0],AL	;DISPLAY OUTPUT
	ADD BL,30
	MOV [C1],BL
	END

