 	CLO
	JMP START
	DB 0		;VARIABLE A = 0
	DB 0		;VARIABLE B = 0
	
START:
	MOV AL,0	;A=0
	MOV [03],AL
	MOV BL,0	;B=0
	MOV [04],BL

PRELOOP:
	CMP AL,3	;A==3?
	JZ LOOP		;IF A=3 GO TO LOOP
	INC AL		;IF NO -> A++
	JMP PRELOOP	;REPEAT PRELOOP


LOOP:			;FOR LOOP
	CMP AL, 6	;A<=5?
	JZ OUTPUT	;IF A>= 6 GO TO OUTPUT
	ADD BL, 3	;B+=3
	MOV [04],BL	;SAVE VALUE OF BL INTO [04] MEMORY CELL
	INC AL		;A++
	JMP LOOP	;REPEAT LOOP

OUTPUT:	
	ADD BL,30	;CONVERT B INTO HEXA-CODE
	MOV [C0],BL	;DISPLAY VALUES
	
	END

